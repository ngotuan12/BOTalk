package com.br.chat.vo;

public class CameraDensity {
	private int w = 0;
	private int h = 0;
	public CameraDensity(int w, int h) {
		super();
		this.w = w;
		this.h = h;
	}
	public int getW() {
		return w;
	}
	public void setW(int w) {
		this.w = w;
	}
	public int getH() {
		return h;
	}
	public void setH(int h) {
		this.h = h;
	}
	
}
